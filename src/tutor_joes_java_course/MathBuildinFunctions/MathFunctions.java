//Math functions
package tutor_joes_java_course.MathBuildinFunctions;

public class MathFunctions {

	public static void main(String[] args) {
		System.out.println("Absolute : "+Math.abs(-38));
		System.out.println("Round : "+Math.round(38.567));
		System.out.println("Ceil : "+Math.ceil(38.238));
		System.out.println("Floor : "+Math.floor(38.235));
		
		int a=20;
		int b=30;
		System.out.println("max : "+Math.max(a,b));
		System.out.println("sqrt : "+Math.sqrt(b));
		System.out.println("power : "+Math.pow(a,b));
		System.out.println("log : "+Math.log(a));
		System.out.println("log10 : "+Math.log10(a));
		System.out.println("sin : "+Math.sin(a));
		System.out.println("cos : "+Math.cos(a));
		System.out.println("tan : "+Math.tan(a));
	}

}
