package Star_Pattern;

public class box_star {

	public static void main(String[] args) {
		int range=5;
        for(int i=1;i<=range;i++){
            for(int j=1;j<=range;j++){
                    System.out.print("* ");
            }
            System.out.println();
        }
	}
}
